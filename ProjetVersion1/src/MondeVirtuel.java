
public class MondeVirtuel {
	private int largeur;
	private int longueur;



	public MondeVirtuel(int largeur, int longueur) {
		super();
		this.largeur = largeur;
		this.longueur = longueur;
	}



	public int getLargeur() {
		return largeur;
	}

	public int getLongueur() {
		return longueur;
	}

}
